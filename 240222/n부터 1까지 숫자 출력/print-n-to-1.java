import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        // 변수 선언, 입력
        int n = sc.nextInt();
        int i = n;

        // 출력
        while (i >= 1) {
            System.out.print(i + " ");
            i--;
        }

        


    }
}